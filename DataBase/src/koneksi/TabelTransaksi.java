    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(confirm == 0){
            try{
                Connection c=koneksidb.getConnection();
                java.sql.PreparedStatement st=c.prepareStatement("delete from tabel_transaksi where Kode_Transaksi = '" + Kodetransaksi.getText()+ "'");
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "pesan", JOptionPane.INFORMATION_MESSAGE);
                AmbilDataBarang();
                BersihData();
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_HapusActionPerformed

    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        try{
            Connection c=koneksidb.getConnection();
            PreparedStatement st=c.prepareStatement("insert into tabel_transaksi(Kode_Transaksi, Kode_Barang, id, Harga, Jumlah, Total) values(?,?,?,?,?,?)");
        st.setString(1, Kodetransaksi.getText());
        st.setString(2, Kodebrg.getText());
        st.setString(3, Id.getText());
        st.setString(4, Harga.getText());
        st.setString(5, Jumlah.getText());
        st.setString(6, Total.getText());
        st.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "pesan", JOptionPane.INFORMATION_MESSAGE);
        AmbilDataBarang();
        BersihData();
        }
    catch(SQLException e){
        System.out.println(e.getMessage());
    }
    }//GEN-LAST:event_SimpanActionPerformed

